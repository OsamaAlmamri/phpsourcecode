// 添加自定义函数，JS 获取 url参数
function GetRequest(url) {
   var str = url.split('?')[1]; //获取url中"?"符后的字串
   var theRequest = new Object();
   // 获取host
   re = /\/\/([\w\.]+)\//i;
   theRequest['host'] = re.exec(url)[1];

   if (str != undefined) {
      strs = str.split("&");
      for(var i = 0; i < strs.length; i ++) {
         theRequest[strs[i].split("=")[0]]=unescape(strs[i].split("=")[1]);
      }
   }
   return theRequest;
}